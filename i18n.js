function t(key) {
  const lang = window._lang || 'fr';
  return (T[lang] && T[lang][key]) || (T['fr'] && T['fr'][key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.dataset.t;
    const val = t(key);
    if (val) el.textContent = val;
  });
  document.querySelectorAll('[data-th]').forEach(el => {
    const key = el.dataset.th;
    const val = t(key);
    if (val) el.innerHTML = val;
  });
  // marquee rebuild
  const raw = t('marquee');
  if (raw) {
    document.querySelectorAll('.marquee').forEach(m => {
      const parts = raw.split(' / ');
      let html = '';
      for (let r = 0; r < 2; r++) {
        parts.forEach((p, i) => {
          html += `<span>${p}</span>`;
          if (i < parts.length - 1) html += `<span class="msep">/</span>`;
        });
      }
      m.innerHTML = html;
    });
  }
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === (window._lang || 'fr'));
  });
  document.documentElement.lang = window._lang || 'fr';
}

function setLang(lang) {
  if (!T[lang]) return;
  window._lang = lang;
  localStorage.setItem('dm_lang', lang);
  applyTranslations();
}


// Init - runs immediately since script is at bottom of body
window._lang = T[localStorage.getItem('dm_lang')] ? localStorage.getItem('dm_lang') : 'fr';
applyTranslations();
document.querySelectorAll('.lang-btn').forEach(function(b) {
  b.addEventListener('click', function() { setLang(b.dataset.lang); });
});
